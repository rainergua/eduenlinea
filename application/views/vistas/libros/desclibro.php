                <h5>Custodio: Casa de la Libertad, FC – BCB, Sucre. </h5>
                Detalles de catalogación o registro: BO CDL, AIAP. <br>
                El acta de la independencia es un documento manuscrito que consta de siete folios, sin la carátula o portada. Los primeros tres folios son un recorrido histórico por la conquista y los cruentos 16 años de lucha por la independencia en el Alto Perú. El cuarto folio contiene un subtítulo que da lugar a la parte resolutiva de la declaración de independencia como “estado soberano e independiente de todas las naciones tanto del viejo como del nuevo mundo”. Para culminar con las firmas de los 47 diputados y la del redactor, José Mariano Serrano.  <br>
                Es por excelencia el documento más representativo del surgimiento de Bolivia a la vida independiente y soberana. En consecuencia, su valor extraordinario ha motivado para que sea el primer documento incorporado en el Registro Nacional de la Memoria del Mundo Bolivia UNESCO, efectuado en la gestión 2018.
